# Function to check if the OS is Windows Server
function Is-WindowsServer {
    $os = Get-WmiObject -Class Win32_OperatingSystem
    return $os.Caption -like "*Server*"
}

# Function to check if the machine is virtualized
function Is-VirtualMachine {
    $computerSystem = Get-WmiObject -Class Win32_ComputerSystem
    $virtualModels = @("Virtual Machine", "VMware Virtual Platform", "VirtualBox", "HVM domU")
    return $virtualModels -contains $computerSystem.Model
}

